public class GajiLembur {
   public static void main(String[] args) {
      // Deklarasi variabel
      int gaji, jamLembur, upahLembur, total;
      
      // Nilai awal
      gaji = 3000000;
      jamLembur = 4;
      upahLembur = 25000;
      
      // Proses perhitungan
      total = gaji + (jamLembur * upahLembur);
      
      // Menampilkan hasil
      System.out.println("Total Gaji + Lembur = " + total);
   }
}